# Qual é o dia da Semana?

dia_semana = int(input("Digite o dia da semana: "))

if dia_semana == 1:
    print("Domingo")
elif dia_semana == 2:
    print("Segunda")
elif dia_semana == 3:
    print("Terça")
elif dia_semana == 4:
    print("Quarta")
elif dia_semana == 5:
    print("Quinta")         
elif dia_semana == 6:
    print("Sexta")
elif dia_semana == 7:
    print("Sábado")
else:
    print("Dia inválido")