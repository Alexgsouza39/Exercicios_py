metros = float('1.5')
centimetros = metros * 100
print(f"{metros} metros equivalem a {centimetros} centímetros.")
# O código acima define uma variável `metros` com o valor de 1.5, converte esse valor para centímetros multiplicando por 100 e, em seguida, imprime a equivalência entre metros e centímetros usando uma f-string para formatar a saída. Quando executado, ele exibirá "1.5 metros equivalem a 150.0 centímetros." no console.
